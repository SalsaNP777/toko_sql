-- 3
SELECT t.KODE, t.TANGGAL, p.NAMA AS NAMA_PELANGGAN, b.NAMA AS NAMA_BARANG, 
t.JUMLAH_BARANG AS JUMLAH, b.HARGA AS HARGA_SATUAN, t.JUMLAH_BARANG * b.HARGA AS TOTAL 
FROM transaksi t
JOIN pelanggan p ON t.KODE_PELANGGAN = p.KODE
JOIN barang b ON t.KODE_BARANG = b.KODE
ORDER BY p.NAMA ASC, t.KODE ASC;

-- 4
SELECT p.NAMA AS NAMA_PELANGGAN, 
SUM(t.JUMLAH_BARANG) AS JUMLAH, 
SUM(t.JUMLAH_BARANG * b.HARGA) AS TOTAL_HARGA  
FROM transaksi t
JOIN pelanggan p ON t.KODE_PELANGGAN = p.KODE
JOIN barang b ON t.KODE_BARANG = b.KODE
GROUP BY p.NAMA;